package chapter02;

//Variable Initialization Example
// 변수 초기화
// 변수를 사용할 때 반드시 변수의 선언 및 초기화 작업이 이루어져야 한다.

public class VariableInitializationExample {
    public static void main(String[] args) {
        // 선언
        int age;
        // 초기화
        age = 10;
        System.out.println(age);

        int value = 0;

        int result = value + 10;

        System.out.println(result);
    }
}
